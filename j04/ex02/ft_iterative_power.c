int		ft_iterative_power(int nb, int power)
{
	int result;
	int i;

	i = 0;
	result = 1;
	if (power == 0)
		return (1);
	else if (power < 0)
		return (0);
	while (power > i)
	{
		result = result * nb;
		i++;
	}
	return (result);
}
