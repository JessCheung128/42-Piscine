int		ft_iterative_factorial(int num)
{
	int b;

	b = 1;
	if (num < 0 || num > 12)
	{
		return (0);
	}
	if (num == 0 || num == 1)
	{
		return (1);
	}
	while (num > 1)
	{
		b *= num;
		num--;
	}
	return (b);
}
